package org.example.models.food;

public class FoodSupply {
    private int wormCount;
    private int wheatCount;
    private int berryCount;
    private int fishCount;
    private int ratCount;

    public int getWormCount() {
        return wormCount;
    }

    public void setWormCount(int wormCount) {
        this.wormCount = wormCount;
    }

    public int getWheatCount() {
        return wheatCount;
    }

    public void setWheatCount(int wheatCount) {
        this.wheatCount = wheatCount;
    }

    public int getBerryCount() {
        return berryCount;
    }

    public void setBerryCount(int berryCount) {
        this.berryCount = berryCount;
    }

    public int getFishCount() {
        return fishCount;
    }

    public void setFishCount(int fishCount) {
        this.fishCount = fishCount;
    }

    public int getRatCount() {
        return ratCount;
    }

    public void setRatCount(int ratCount) {
        this.ratCount = ratCount;
    }
}
